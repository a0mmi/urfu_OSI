F=("" "1;" "2;" "3;" "4;" "5;" "6;" "7;" "8;" "9;")
T=("30" "31" "32" "33" "34" "35" "36" "37" "90" "91" "92" "93" "94" "95" "96" "97")
B=("" "40;" "41;" "42;" "43;" "44;" "45;" "46;" "47;" "100;" "101;" "102;" "103;" "104;" "105;" "106;" "107;")

for b in "${B[@]}"; do
  for t in "${T[@]}"; do
    for f in "${F[@]}"; do
      code="${b}${f}${t}"
      esc="\e[${code%;}m"
      printf '%b' "${esc}<3\e[0m"
    done
  done
  printf '\n'
done
